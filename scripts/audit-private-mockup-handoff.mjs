#!/usr/bin/env node
import { execFile } from 'node:child_process';
import { promisify } from 'node:util';
import path from 'node:path';

const execFileAsync = promisify(execFile);

const DEFAULT_RACE_TEMPLATES_DIR = '../race-templates';
const MAX_OWNER_PREVIEW_CHARS = 3800;
const MOCKUP_TOKEN_PATTERN = /^[a-zA-Z0-9_-]{16,}$/;
const USAGE = 'Usage: npm run audit:private-mockup -- --audit-id <uuid> [--slug <slug>] [--mockup-token <token> | --mockup-url <tokenized-url>] [--no-generate] [--dry-run] [--send-owner-preview]';

const parseArgs = (argv = process.argv.slice(2)) => {
  const args = {};
  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (!token.startsWith('--')) continue;
    const key = token.slice(2);
    const next = argv[index + 1];
    if (!next || next.startsWith('--')) {
      args[key] = true;
    } else {
      args[key] = next;
      index += 1;
    }
  }
  return args;
};

const clean = (value, max = 500) => {
  if (typeof value !== 'string') return '';
  return value.trim().replace(/\s+/g, ' ').slice(0, max);
};

export const isHttpUrl = (value) => {
  try {
    const url = new URL(value);
    return url.protocol === 'http:' || url.protocol === 'https:';
  } catch {
    return false;
  }
};

export const slugify = (value, fallback = 'audit-private-mockup') => {
  const slug = clean(value, 120)
    .toLowerCase()
    .replace(/&/g, ' and ')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 72)
    .replace(/-+$/g, '');
  return slug || fallback;
};

const joinUrl = (baseUrl, route) => {
  const base = clean(baseUrl, 1000).replace(/\/$/, '');
  const routePath = clean(route, 1000).startsWith('/') ? clean(route, 1000) : `/${clean(route, 1000)}`;
  return `${base}${routePath}`;
};

export const isOriginOnlyUrl = (value) => {
  try {
    const url = new URL(value);
    return (url.protocol === 'http:' || url.protocol === 'https:')
      && (url.pathname === '' || url.pathname === '/')
      && !url.search
      && !url.hash;
  } catch {
    return false;
  }
};

export const isTokenizedPrivateMockupUrl = (value) => {
  try {
    const url = new URL(value);
    if (!isHttpUrl(value)) return false;

    const segments = url.pathname.split('/').filter(Boolean);
    const isPrivateMockupPath = segments[0] === 'private' && segments[1] === 'mockups';
    if (!isPrivateMockupPath || segments.length < 3) return false;

    const tokenFromPath = segments[3];
    const tokenFromQuery = url.searchParams.get('token') || url.searchParams.get('preview_token');
    return MOCKUP_TOKEN_PATTERN.test(tokenFromPath || tokenFromQuery || '');
  } catch {
    return false;
  }
};

const routeFromPrivateMockupUrl = (value, fallbackRoute) => {
  try {
    const url = new URL(value);
    return `${url.pathname}${url.search}`;
  } catch {
    return fallbackRoute;
  }
};

export const buildPrivateMockupUrl = ({ baseUrl, slug, token, explicitUrl }) => {
  if (explicitUrl) {
    if (!isHttpUrl(explicitUrl)) throw new Error('--mockup-url must be a valid http(s) URL.');
    if (!isTokenizedPrivateMockupUrl(explicitUrl)) {
      throw new Error('--mockup-url must be a tokenized private mockup URL, not a predictable slug or mockup library URL.');
    }
    return explicitUrl;
  }

  if (!baseUrl || !isOriginOnlyUrl(baseUrl)) {
    throw new Error('Set STARTLINE_PRIVATE_MOCKUP_BASE_URL to an http(s) origin only, such as https://mockups.startlinesites.com, or pass --mockup-url after a race-templates branch/deploy preview exists.');
  }

  const cleanToken = clean(token, 200);
  if (!MOCKUP_TOKEN_PATTERN.test(cleanToken)) {
    throw new Error('Pass --mockup-token with the private token generated by race-templates, or pass the full tokenized --mockup-url. Predictable slug-only mockup URLs are not allowed.');
  }

  return joinUrl(baseUrl, `/private/mockups/${slug}/${cleanToken}/`);
};

export const mergeAuditWorkflowMetadata = ({ currentMetadata = {}, privateMockupUrl, slug, sourceUrl, generatedAt, route }) => ({
  ...currentMetadata,
  audit_workflow: {
    ...(currentMetadata?.audit_workflow || {}),
    private_mockup_url: privateMockupUrl,
    private_mockup_slug: slug,
    private_mockup_route: route,
    private_mockup_source_url: sourceUrl,
    private_mockup_generated_at: generatedAt,
    private_mockup_status: 'steve_review_only',
    customer_delivery_blocked_until: 'steve_approval',
  },
});

export const mergeAuditSummary = ({ currentSummary = {}, privateMockupUrl, generatedAt }) => ({
  ...(currentSummary || {}),
  private_mockup_url: privateMockupUrl,
  private_mockup_generated_at: generatedAt,
  private_mockup_status: 'steve_review_only',
});

export const buildOwnerPreviewText = ({ record, privateMockupUrl, route, generated, patchStored }) => {
  const raceName = clean(record?.race_name, 160) || 'Unknown race';
  const currentUrl = clean(record?.current_url, 500) || 'Not provided';
  const contactName = clean(record?.contact_name, 160) || 'Not provided';
  const contactEmail = clean(record?.contact_email, 254) || 'Not provided';
  const notes = clean(record?.notes, 900) || 'None';
  const selectedPackage = record?.metadata?.selected_package?.name || null;

  const lines = [
    `StartLine owner preview ready: ${raceName}`,
    '',
    `Private mockup URL: ${privateMockupUrl}`,
    `Private route: ${route}`,
    `Generation: ${generated ? 'generated/updated in race-templates' : 'URL stored only (generation skipped or already done)'}`,
    `Supabase update: ${patchStored ? 'stored on audit record' : 'not stored (dry run)'}`,
    '',
    'Review gate:',
    '- Steve approval is required before any customer delivery.',
    '- Do not send this private mockup URL to the race director until Steve approves the handoff.',
    '- Customer delivery remains blocked; use public-source assets only and confirm the concept is respectful.',
    '',
    'Audit record:',
    `- Race: ${raceName}`,
    `- Current URL: ${currentUrl}`,
    `- Contact: ${contactName} <${contactEmail}>`,
    `- Selected first-year package: ${selectedPackage || 'Not selected / recommend after review'}`,
    `- Notes: ${notes}`,
  ];

  const text = lines.join('\n');
  return text.length > MAX_OWNER_PREVIEW_CHARS ? `${text.slice(0, MAX_OWNER_PREVIEW_CHARS - 20)}\n…[truncated]` : text;
};

const getSupabaseConfig = () => {
  const supabaseUrl = process.env.SUPABASE_URL?.replace(/\/$/, '');
  const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!supabaseUrl || !serviceKey) {
    throw new Error('SUPABASE_URL and SUPABASE_SERVICE_ROLE_KEY are required.');
  }
  return { supabaseUrl, serviceKey };
};

const fetchAuditRecord = async ({ auditId, supabaseUrl, serviceKey }) => {
  const response = await fetch(`${supabaseUrl}/rest/v1/audit_requests?id=eq.${encodeURIComponent(auditId)}&select=*`, {
    headers: {
      apikey: serviceKey,
      authorization: `Bearer ${serviceKey}`,
      accept: 'application/json',
    },
  });

  if (!response.ok) {
    const detail = await response.text();
    throw new Error(`Supabase audit fetch failed: ${response.status} ${detail}`);
  }

  const rows = await response.json();
  if (!rows.length) throw new Error(`No audit_requests row found for id ${auditId}.`);
  return rows[0];
};

const patchAuditRecord = async ({ auditId, supabaseUrl, serviceKey, metadata, auditSummary, privateMockupUrl }) => {
  const response = await fetch(`${supabaseUrl}/rest/v1/audit_requests?id=eq.${encodeURIComponent(auditId)}`, {
    method: 'PATCH',
    headers: {
      apikey: serviceKey,
      authorization: `Bearer ${serviceKey}`,
      'content-type': 'application/json',
      prefer: 'return=minimal',
    },
    body: JSON.stringify({
      private_mockup_url: privateMockupUrl,
      metadata,
      audit_summary: auditSummary,
    }),
  });

  if (!response.ok) {
    const detail = await response.text();
    throw new Error(`Supabase audit patch failed: ${response.status} ${detail}`);
  }
};

const runPrivateMockupGenerator = async ({ raceTemplatesDir, sourceUrl, slug, token, template }) => {
  const args = ['run', 'mockup:private', '--', '--url', sourceUrl, '--slug', slug];
  if (token) args.push('--token', token);
  if (template) args.push('--template', template);

  const { stdout, stderr } = await execFileAsync('npm', args, {
    cwd: raceTemplatesDir,
    maxBuffer: 1024 * 1024,
  });

  return { stdout, stderr };
};

const sendOwnerEmail = async ({ previewText, record }) => {
  const apiKey = process.env.RESEND_API_KEY || process.env.STARTLINE_RESEND_API_KEY;
  const to = process.env.STARTLINE_OWNER_PREVIEW_EMAIL || process.env.STARTLINE_ADMIN_EMAIL || process.env.STARTLINE_LEAD_NOTIFY_EMAIL;
  if (!apiKey || !to) return false;

  const from = process.env.STARTLINE_NOTIFY_FROM || 'StartLine Sites <support@startlinesites.com>';
  const response = await fetch('https://api.resend.com/emails', {
    method: 'POST',
    headers: {
      authorization: `Bearer ${apiKey}`,
      'content-type': 'application/json',
      'user-agent': 'StartLineSites/1.0 (audit-private-mockup-handoff)',
    },
    body: JSON.stringify({
      from,
      to: [to],
      subject: `Owner preview: ${clean(record?.race_name, 120) || 'StartLine audit mockup'}`,
      text: previewText,
      html: `<pre style="white-space:pre-wrap;font-family:ui-monospace,SFMono-Regular,Menlo,monospace">${previewText.replaceAll('&', '&amp;').replaceAll('<', '&lt;').replaceAll('>', '&gt;')}</pre>`,
    }),
  });

  if (!response.ok) {
    const detail = await response.text();
    throw new Error(`Owner preview email failed: ${response.status} ${detail}`);
  }
  return true;
};

const sendOwnerTelegram = async ({ previewText }) => {
  const token = process.env.STARTLINE_TELEGRAM_BOT_TOKEN;
  const chatId = process.env.STARTLINE_TELEGRAM_OWNER_CHAT_ID || process.env.STARTLINE_TELEGRAM_CHAT_ID;
  if (!token || !chatId) return false;

  const response = await fetch(`https://api.telegram.org/bot${token}/sendMessage`, {
    method: 'POST',
    headers: { 'content-type': 'application/json' },
    body: JSON.stringify({
      chat_id: chatId,
      text: previewText,
      disable_web_page_preview: true,
    }),
  });

  if (!response.ok) {
    const detail = await response.text();
    throw new Error(`Owner preview Telegram failed: ${response.status} ${detail}`);
  }
  return true;
};

const main = async () => {
  const args = parseArgs();
  if (args.help || args.h) {
    console.log(USAGE);
    console.log('STARTLINE_PRIVATE_MOCKUP_BASE_URL must be an http(s) origin only; slug-only private mockup URLs are rejected.');
    console.log('Customer delivery is never automatic: --send-owner-preview only sends the Steve owner preview gate.');
    return;
  }
  const auditId = clean(args['audit-id'] || args.id, 120);
  if (!auditId) throw new Error(USAGE);

  const { supabaseUrl, serviceKey } = getSupabaseConfig();
  const record = await fetchAuditRecord({ auditId, supabaseUrl, serviceKey });
  const sourceUrl = clean(args.url || record.current_url, 1000);
  if (!isHttpUrl(sourceUrl)) throw new Error('Audit record current_url must be a valid http(s) URL, or pass --url.');

  const slugBase = args.slug || `${record.race_name || 'race'}-${String(record.id || auditId).slice(0, 8)}`;
  const slug = slugify(slugBase);
  const mockupToken = clean(args['mockup-token'] || args.token, 200);
  const composedRoute = mockupToken ? `/private/mockups/${slug}/${mockupToken}/` : `/private/mockups/${slug}/`;
  const generatedAt = new Date().toISOString();
  const raceTemplatesDir = path.resolve(process.cwd(), clean(args['race-templates-dir'] || process.env.STARTLINE_RACE_TEMPLATES_DIR || DEFAULT_RACE_TEMPLATES_DIR, 1000));
  const privateMockupUrl = buildPrivateMockupUrl({
    baseUrl: process.env.STARTLINE_PRIVATE_MOCKUP_BASE_URL,
    slug,
    token: mockupToken,
    explicitUrl: args['mockup-url'],
  });
  const route = routeFromPrivateMockupUrl(privateMockupUrl, composedRoute);

  let generated = false;
  if (!args['no-generate']) {
    const result = await runPrivateMockupGenerator({
      raceTemplatesDir,
      sourceUrl,
      slug,
      token: mockupToken,
      template: clean(args.template, 40),
    });
    generated = true;
    if (result.stdout) process.stdout.write(result.stdout);
    if (result.stderr) process.stderr.write(result.stderr);
  }

  const metadata = mergeAuditWorkflowMetadata({
    currentMetadata: record.metadata || {},
    privateMockupUrl,
    slug,
    sourceUrl,
    generatedAt,
    route,
  });
  const auditSummary = mergeAuditSummary({
    currentSummary: record.audit_summary || {},
    privateMockupUrl,
    generatedAt,
  });

  const dryRun = Boolean(args['dry-run']);
  if (!dryRun) {
    await patchAuditRecord({ auditId, supabaseUrl, serviceKey, metadata, auditSummary, privateMockupUrl });
  }

  const previewText = buildOwnerPreviewText({
    record,
    privateMockupUrl,
    route,
    generated,
    patchStored: !dryRun,
  });

  let emailSent = false;
  let telegramSent = false;
  if (args['send-owner-preview']) {
    emailSent = await sendOwnerEmail({ previewText, record });
    telegramSent = await sendOwnerTelegram({ previewText });
  }

  console.log('\n--- Steve owner preview handoff ---');
  console.log(previewText);
  console.log('--- End owner preview handoff ---');
  console.log(`Owner email sent: ${emailSent ? 'yes' : 'no'}`);
  console.log(`Owner Telegram sent: ${telegramSent ? 'yes' : 'no'}`);
};

if (import.meta.url === `file://${process.argv[1]}`) {
  main().catch((error) => {
    console.error(error.message);
    process.exit(1);
  });
}
